//Declarasion de función
function sumar() {
    console.log( 2 + 2 );
}
sumar();

//Expresion de funcion (Function Expression)

const sumar2 = function() {
    console.log( 3 + 3 );
}
sumar2();

//Diferencias entre "Expresión de Función" y "Declarar una Función"
//En la declarativa de Función nos permite ejercutar la función antes de crearlas gracias al hosting
//En la Expresión de Función no podemos hacerlo ya que es como hacerlo una variable 
